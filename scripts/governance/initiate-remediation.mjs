#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";
import { spawnSync } from "node:child_process";

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (!token.startsWith("--")) continue;
    const key = token.slice(2);
    const value = argv[index + 1];
    if (!value || value.startsWith("--")) {
      args[key] = true;
      continue;
    }
    args[key] = value;
    index += 1;
  }
  return args;
}

function runGh(args, allowFailure = false) {
  const result = spawnSync("gh", args, { encoding: "utf8" });
  if (result.error) {
    throw result.error;
  }
  if (result.status !== 0) {
    if (allowFailure) {
      return null;
    }
    throw new Error((result.stderr ?? "").trim() || `gh ${args.join(" ")} failed.`);
  }
  return (result.stdout ?? "").trim();
}

function loadReport(path) {
  if (!existsSync(path)) {
    throw new Error(`Report not found at ${path}. Run governance:audit first.`);
  }
  return JSON.parse(readFileSync(path, "utf8"));
}

const args = parseArgs(process.argv.slice(2));
const owner = String(args.owner ?? "").trim();
const reportPath = String(args.report ?? "docs/reports/repository-security-audit.json");
const outputPath = String(args.output ?? "docs/remediation-initiation-summary.md");
const execute = Boolean(args.execute);
const maxRepos = Number.parseInt(String(args["max-repos"] ?? "9999"), 10);

if (!owner) {
  throw new Error("Missing required --owner argument.");
}

const report = loadReport(reportPath);
const records = (report.records ?? [])
  .filter((record) => !record.error && !record.productionReady)
  .sort((a, b) => a.compliancePercent - b.compliancePercent)
  .slice(0, maxRepos);

const title = "[Security Readiness] Portfolio remediation checklist";
const created = [];
const existing = [];
const failures = [];
const dryRun = [];

function checklistFor(record) {
  const controls = record.controls ?? {};
  const missing = Object.entries(controls)
    .filter(([, enabled]) => !enabled)
    .map(([control]) => control);

  const mapToTask = {
    dependabot: "Add `.github/dependabot.yml` and enable update cadence.",
    securityPolicy: "Add `SECURITY.md` with disclosure and patch policy.",
    ciWorkflow: "Add CI workflow for build + tests with required check status.",
    securityWorkflow: "Add security workflow (CVE audit gate and scheduled scans).",
    codeqlWorkflow: "Enable CodeQL workflow and code scanning upload.",
    sbomWorkflow: "Add SBOM generation workflow and artifact retention.",
    secretScanWorkflow: "Add secret scanning workflow (gitleaks/trufflehog).",
    scorecardWorkflow: "Add OSSF scorecard workflow for supply-chain posture.",
    installableDependencyModel:
      "Remove `workspace:*` anti-pattern for shared-core; use published version or optional peer dependency.",
  };

  const lines = [];
  lines.push("This issue was generated by the portfolio governance automation.");
  lines.push("");
  lines.push(`Current compliance score: **${record.compliancePercent}%**`);
  lines.push("");
  lines.push("## Required actions");
  lines.push("");
  for (const key of missing) {
    lines.push(`- [ ] ${mapToTask[key] ?? `Resolve control gap: ${key}`}`);
  }
  lines.push("");
  lines.push("## Exit criteria");
  lines.push("");
  lines.push("- [ ] Repo reaches 100% control compliance.");
  lines.push("- [ ] Security and CI checks are required on default branch.");
  lines.push("- [ ] Dependency model is installable outside monorepo context.");
  lines.push("");
  lines.push("_Generated by `scripts/governance/initiate-remediation.mjs`._");
  return lines.join("\n");
}

for (const record of records) {
  const repoRef = `${owner}/${record.repo}`;
  const existingIssueRaw = runGh(
    [
      "issue",
      "list",
      "-R",
      repoRef,
      "--state",
      "open",
      "--search",
      `${title} in:title`,
      "--json",
      "number,title,url",
    ],
    true,
  );
  const openIssues = existingIssueRaw ? JSON.parse(existingIssueRaw) : [];

  if (openIssues.length > 0) {
    existing.push({
      repo: record.repo,
      issue: openIssues[0].url,
    });
    continue;
  }

  const body = checklistFor(record);
  if (!execute) {
    dryRun.push({
      repo: record.repo,
      previewTasks: body.split("\n").filter((line) => line.startsWith("- [ ] ")).length,
    });
    continue;
  }

  const createResult = runGh(
    [
      "issue",
      "create",
      "-R",
      repoRef,
      "--title",
      title,
      "--body",
      body,
    ],
    true,
  );

  if (!createResult) {
    failures.push({ repo: record.repo, reason: "issue create command failed" });
    continue;
  }

  created.push({ repo: record.repo, issue: createResult.trim() });
}

const lines = [];
lines.push("# Remediation Initiation Summary");
lines.push("");
lines.push(`Generated: ${new Date().toISOString()}`);
lines.push(`Owner: \`${owner}\``);
lines.push(`Mode: **${execute ? "execute" : "dry-run"}**`);
lines.push("");
lines.push(`Target repositories: **${records.length}**`);
lines.push(`Issues created: **${created.length}**`);
lines.push(`Existing open issues reused: **${existing.length}**`);
lines.push(`Failures: **${failures.length}**`);
lines.push(`Dry-run previews: **${dryRun.length}**`);
lines.push("");

if (created.length > 0) {
  lines.push("## Created");
  lines.push("");
  for (const item of created) {
    lines.push(`- ${item.repo}: ${item.issue}`);
  }
  lines.push("");
}

if (existing.length > 0) {
  lines.push("## Existing open issues");
  lines.push("");
  for (const item of existing) {
    lines.push(`- ${item.repo}: ${item.issue}`);
  }
  lines.push("");
}

if (dryRun.length > 0) {
  lines.push("## Dry-run targets");
  lines.push("");
  for (const item of dryRun) {
    lines.push(`- ${item.repo}: ${item.previewTasks} checklist items`);
  }
  lines.push("");
}

if (failures.length > 0) {
  lines.push("## Failures");
  lines.push("");
  for (const item of failures) {
    lines.push(`- ${item.repo}: ${item.reason}`);
  }
  lines.push("");
}

const summary = lines.join("\n");
mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, summary, "utf8");
console.log(summary);
console.log(`Saved remediation initiation summary to ${outputPath}`);
